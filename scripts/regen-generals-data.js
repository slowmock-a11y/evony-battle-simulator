// Regenerates js/generals-data.js and js/covenants-data.js from the data/ JSON files.
// Run after editing data/generals.json or data/covenants.json:
//   node scripts/regen-generals-data.js

const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');

function regen(sourceRel, outRel, globalName) {
    const sourcePath = path.join(repoRoot, sourceRel);
    if (!fs.existsSync(sourcePath)) {
        console.log('Skipping', outRel, '- source', sourceRel, 'not found');
        return;
    }
    const data = JSON.parse(fs.readFileSync(sourcePath, 'utf-8'));
    const header = [
        '// Auto-generated from ' + sourceRel + ' by scripts/regen-generals-data.js.',
        '// Do not edit this file directly. Edit ' + sourceRel + ' and run:',
        '//   node scripts/regen-generals-data.js',
        ''
    ].join('\n');
    const body = 'var ' + globalName + ' = ' + JSON.stringify(data, null, 2) + ';\n';
    const outPath = path.join(repoRoot, outRel);
    fs.writeFileSync(outPath, header + '\n' + body);
    const count = Array.isArray(data) ? data.length : Object.keys(data).length;
    console.log('Wrote', outPath, '-', fs.statSync(outPath).size, 'bytes,', count, 'entries');
}

regen('data/generals.json',  'js/generals-data.js',  'GENERALS_DATA');
regen('data/covenants.json', 'js/covenants-data.js', 'COVENANTS_DATA');
